package com.myfistapp.login_form;

public class ClassDiaChi {
    private String hoten, sdt, thanhpho, huyen, xa, sonha, macdinh;

    public ClassDiaChi(String hoten, String sdt, String thanhpho, String huyen, String xa, String sonha, String macdinh) {
        this.hoten = hoten;
        this.sdt = sdt;
        this.thanhpho = thanhpho;
        this.huyen = huyen;
        this.xa = xa;
        this.sonha = sonha;
        this.macdinh = macdinh;
    }

    public String getHoten() {
        return hoten;
    }

    public void setHoten(String hoten) {
        this.hoten = hoten;
    }

    public String getSdt() {
        return sdt;
    }

    public void setSdt(String sdt) {
        this.sdt = sdt;
    }

    public String getThanhpho() {
        return thanhpho;
    }

    public void setThanhpho(String thanhpho) {
        this.thanhpho = thanhpho;
    }

    public String getHuyen() {
        return huyen;
    }

    public void setHuyen(String huyen) {
        this.huyen = huyen;
    }

    public String getXa() {
        return xa;
    }

    public void setXa(String xa) {
        this.xa = xa;
    }

    public String getSonha() {
        return sonha;
    }

    public void setSonha(String sonha) {
        this.sonha = sonha;
    }

    public String getMacdinh() {
        return macdinh;
    }

    public void setMacdinh(String macdinh) {
        this.macdinh = macdinh;
    }
}
